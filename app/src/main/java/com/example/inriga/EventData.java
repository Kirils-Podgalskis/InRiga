package com.example.inriga;

public class EventData {
    String name;
    String description;
    String date;
    String time;
    String location;

    public EventData(String name, String description, String date, String time, String location) {
        this.name = name;
        this.description = description;
        this.date = date;
        this.time = time;
        this.location = location;
    }
}
